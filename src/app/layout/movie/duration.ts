export class Duration {

    date: 0;
    day: 0;
    hours: 0;
    minutes: 0;
    month: 0;
    seconds: 0;
    time: 0;
    timezoneOffset: 0;
    year: 0
    constructor(
        date: 0,
        day: 0,
        hours: 0,
        minutes: 0,
        month: 0,
        seconds: 0,
        time: 0,
        timezoneOffset: 0,
        year: 0) {
        this.date = date;
        this.day = day;
        this.hours = hours;
        this.minutes = minutes;
        this.month = month;
        this.seconds = seconds;
        this.time = time;
        this.timezoneOffset = timezoneOffset;
        this.year = year;

    }

}